#include <stdio.h>
#include <stdlib.h>

int main()
{
    printf("%s is the best %s ever. \n", "Bucky", "programmer"); // string

    printf("I ate %d corndogs last night \n", 9); // integer

    printf("Pi is %f \n", 3.14535789); // float
    printf("Pi is %.2f \n", 3.14535789); // show 2 decimals
    printf("Pi is %.4f \n", 3.14535789); // show 4 decimals

    return 0;
}
