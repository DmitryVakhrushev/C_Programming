#include <stdio.h>
#include <stdlib.h>

int main()
{
    int weight = 595;

    // Operators: *, /, +, -, %
    printf("If I eat a watermelon I will weigh %d lbs \n", weight + 12);

    // INTEGRES
    int a = 86;
    int b = 21;
    printf("%d \n", a/b);

    // FLOATS
    float c = 86.0;
    float d = 21.0;
    printf("%f \n", c/d);

    return 0;
}
